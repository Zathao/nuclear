	Q.generateLevel = function( id, stage ){

		// Loading level
		var level = Q.levels[ id ];						
		var objects = level.objects;

		// Tutorial
		if( level.tutorialHand ){
			
			stage.insert( new Q.TutorialHand( { x: level.tutorialHand.x, y: level.tutorialHand.y } ) );
			stage.insert( new Q.TutorialMask() );
			
		} 

		// Gerenerating level objects
		for( var i=0; i<objects.length; i++ ){
			for( var j=0; j<objects[i].length; j++ ){

				var object = objects[i][j].object;
				var direction = objects[i][j].direction;

				if( object && direction ){

					var directions = { 
						up: { angle: 0, x: 0, y: -1 },
						right: { angle: 90, x: 1, y: 0 },
						down: { angle: 180, x: 0, y: 1  },
						left: { angle: 270, x: -1, y: 0 }
					};

					stage.insert( new Q[ object ]( { x: j*120+60, y: i*120+60, direction: directions[ direction ] } ) );

				}

			}
		} 

		// Creating Background
		stage.insert( new Q.Sprite( { asset: level.background, x: Q.width/2, y: Q.height/2, type: Q.SPRITE_UI } ) );

	}

